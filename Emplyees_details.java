    private void srchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_srchActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = obj.search(Integer.parseInt(id.getText()));
            if (rs.next()) {
                String employeeid = Integer.toString(rs.getInt(1));
                String employeename = rs.getString(2);
                String employeecontact = rs.getString(3);
                String employeeaddress = rs.getString(4);
                String d = info.getText() + employeeid + "\t" + employeename + "\t" +
                    employeecontact + "\t" + employeeaddress + "\n";
                info.setText(d);
                oper.setText("Record found!");
            }
            else {
                oper.setText("Record not found!");
            }
        } catch (Exception ex) {
            oper.setText(ex.getMessage());
        } 
    }//GEN-LAST:event_srchActionPerformed

