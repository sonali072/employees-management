    private void disActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_disActionPerformed
        // TODO add your handling code here:
        try {
            ResultSet rs = obj.display();
            if(rs != null){
                while(rs.next()) {
                    String employeeid = Integer.toString(rs.getInt(1));
                    String employeename = rs.getString(2);
                    String employeecontact = rs.getString(3);
                    String employeeaddress = rs.getString(4);
                    String d = info.getText() + employeeid + "\t" + employeename + "\t" +
                            employeecontact + "\t" + employeeaddress + "\n";
                    info.setText(d);
                }
            } else {
                info.setText("No information available!");
            }
            oper.setText("Displayed successfully!");
        } catch(Exception ex) {
            oper.setText(ex.getMessage());
        }
    }//GEN-LAST:event_disActionPerformed

